<h1>History of RSS</h1>


<div class="timeline">

    <TimelineEntry year="1995" title="RDF Site Summary (RSS 0.9)">
        RSS was first introduced by Netscape in 1995, for use in the My Netscape Network portal. It was known as RDF Site Summary and later renamed to Rich Site Summary.
    </TimelineEntry>

    <TimelineEntry year="1999" title="RSS 0.91">
        The first version of RSS was released by Netscape in 1999. It was based on RDF Site Summary, but simplified and renamed to Really Simple Syndication.
    </TimelineEntry>

    <TimelineEntry year="2000" title="RSS 0.92">
        RSS 0.92 was released in 2000. It was the last version of RSS to be released by Netscape.
    </TimelineEntry>

    <TimelineEntry year="2002" title="RSS 2.0">
        RSS 2.0 was released in 2002. It was created by Dave Winer and became the most widely used version of RSS.
    </TimelineEntry>

    <TimelineEntry year="2003" title="RSS 2.0.1">
        RSS 2.0.1 was released in 2003. It was a minor update to RSS 2.0.
    </TimelineEntry>

    <TimelineEntry year="2005" title="RSS 2.0.2">
        RSS 2.0.2 was released in 2005. It was a minor update to RSS 2.0.1.
    </TimelineEntry>

    <TimelineEntry year="2007" title="RSS 2.0.3">
        RSS 2.0.3 was released in 2007. It was a minor update to RSS 2.0.2.
    </TimelineEntry>

    <TimelineEntry year="2009" title="RSS 2.0.4">
        RSS 2.0.4 was released in 2009. It was a minor update to RSS 2.0.3.
    </TimelineEntry>

    <TimelineEntry year="2011" title="RSS 2.0.5">
        RSS 2.0.5 was released in 2011. It was a minor update to RSS 2.0.4.
    </TimelineEntry>

    <TimelineEntry year="2019" title="RSS 2.0.11">
        RSS 2.0.11 was released in 2019. It was a minor update to RSS 2.0.5.
    </TimelineEntry>

    <TimelineEntry year="2020" title="RSS 2.0.12">
        RSS 2.0.12 was released in 2020. It was a minor update to RSS 2.0.11.
    </TimelineEntry>

    <TimelineEntry year="2021" title="RSS 2.0.13">
        RSS 2.0.13 was released in 2021. It was a minor update to RSS 2.0.12.
    </TimelineEntry>

